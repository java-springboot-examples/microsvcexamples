package microsvc.sb;

public class ServiceConfig {

    public final static String HTTP_HEADER_CORRELATION_ID = "x-correlation-id";


    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}